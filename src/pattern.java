

public class pattern {
    public static void main(String[] args) {


        System.out.println("Pattern 1 :");
        for(int i=0;i<=5;i++){
            for (int j=1;j<=i;j++){
                System.out.print("* ");
            }
            System.out.println();
        }
        for(int i =0;i<3;i++){
            for(int j =0;j<5;j++){
                System.out.print("4 ");
            }
            System.out.println();
        }


    }
}
